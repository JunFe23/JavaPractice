package sist.com.obj.basic1;

public class Animal {
	private String name;
	private int leg;
	private String color;
	

	public void setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;
	}
	public void setLeg(int leg) {
		this.leg=leg;
	}
	public int getLeg() {
		return leg;
	}
	public void setColor(String color) {
		this.color=color;
	}
	public String getColor() {
		return color;
	}
	
}
